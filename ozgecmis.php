Fulyağ Petrol A.Ş. , 2006 yılında Saffet Yaylamış tarafından kurulmuş olup ,2006 yılından bu yana eğitimli uzman kadrosuyla endüstriyel madeni yağ satışı ve satış sonrası teknik destek hizmeti ile Ege Bölgesinde lider konumuna erişmiştir. Firmamız Fulyağ Petrol A.Ş. 2006 yılında Opet Fuchs Madeni Yağ A.Ş.'nin Ege Bölge Distribütörlüğü'nü üstlenmiş olup İzmir merkezli faaliyetlerini Manisa, Aydın, Denizli, Uşak ve Muğla bölgelerinde de 10 yıl boyunca başarı ile sürdürmüştür. 2017 yılı itibariyle yoluna kaliteli ve çok çeșitli ürünlerle sektördeki varlığını daha da güçlenerek devam ettirmektedir. Sürekli iyileştirme ve müşteri memnuniyetini prensip edinmiş olan şirketimiz, uzman danışmanlarımız ile mühendislik çözümler üreterek, minimum maliyet ile maksimum verim sağlayarak, birinci sınıf lojistik hizmeti ve servis ağı sunarak, her yerde daima ulaşılabilen bir “ÇÖZÜM ORTAĞINIZ” olmak öncelikli amacımızdır

Fulyağ Petrol A.Ş. olarak şirket prensibimiz, satış pazarlama kurgumuz müşterilerimize çözüm odaklı farklı yenilikçi faydalar sunmaktır. Bunlar; tamamı üniversite mezunu teknik ekip, lojistik sevkiyat gücü, finansal vade gücü, düzenli teknik servis ve takip, laboratuvar ve ürün analiz hizmetleri, konularında uzman satış personeli, kazan kazan modelinin yaşatılması, hızlı geri dönüş, 2700 metrekare depo ve ofis alanı, 400 tona yakın stok ile müşterilerimizin stok maliyetinden kurtarmak, bölgesel satış sorumlularının düzenli bölge takibi, mühendislik satışı yaparak sorun çözme kabiliyeti, firmalarda yağ kullanım oranları üzerine çalışma yapıp kullanım oranlarının düşürülmesi, çevre ve insan sağlığına faydalı ürünlerin pazara sokulması ve yenilikçi ürünlerin araştırılması olarak sıralayabiliriz. 2006 yılında 100 ton/yıl ürün satışıyla yola çıkan Fulyağ petrol, 2015 yılı itibariyle sadece Ege bölgesinde 2500 ton /yıl ürün satışına ulaşmıştır.
2017 yılı itibariyle Almanya menşeili ADDİNOL endüstriyel yağları firmasının Türkiye distribütörlüğünü almış bulunuyoruz. Dünya üzerinde rakiplerinin çok üst sıralarında bulunan firma Türkiye pazarında en çok onayı bulunan firma olarak üst performans ürünlerinin tedarikini sağlamaktadır
2017 yılı itibariyle yine Almanya menşeili WİLKE endüstriyel yağlar Ege bölge distribütörlüğünü bünyemize katmış bulunuyoruz. Wilke firması Almanya merkezli olup üç farklı kardeş (ortak ) firmadır.Bunlar CURTİS SYSTEMS endüstriyel yağlar ve ML LUBRİCANT TIR.
2017 yılı itibariyle yine Amerika menşeili 3M firmasının aşındırıcı taşlar ve zımpara grubu Ege bölge distribütörlüğü almış bulunmaktayız 
Yeni pazarlar ve ürünler anlamında yerli üretime de önem gösteren firmamız tescilli markamız olan TRİBOLİNE markası ile kendi markamızı yaratmış durumdayız. Triboline markası yüzde yüz yerli sermaye yatırım olup, Fulyağ petrol a.ş. tarafından projelendirilmiştir.. Bu sayede endüstriyel pazarda dışa bağımlılığın azaltılması prensibine yoğunlaşan firmamız yoğun saha tecrübesi ile çözüm odaklı ve uygun maliyetli ürünler sunmaktadır.Hedefimiz hızla büyüyerek istihdama endeksli, pazarın ihtiyacını karşılayacak yatırımlar yapmaktır.Fulyağ prensipleri arasında yine üreten ülke modeli bulunmaktadır. Triboline markası İzmir ile beraber İstanbul ,Bursa, Ankara, Konya ,Diyarbakır ve daha birçok ilde yoğun olarak kullanılmaktadır. Triboline marka ürünlerimizin bir kısmı TÜBİTAK argeleri kapsamında ürün geliştirme arge projeleri yapılmaktadır.
Fulyağ petrol olarak Ulukent sanayi bölgesindeki binamızda bulunan laboratuvar tesisimiz gün geçtikçe yenilenen en son sistem cihazlar ile müşterilerimizin hizmetine devam etmektedir. Özellikle kullanılmış yağların analizleri yapılarak gerek ömür sürelerinin uzatılması , gerekse çevre ve insan sağlığı açısından etkilerinin azaltılması sağlanmaktadır. 
2006 yılında 1 kişi ile yol çıkan firmamız 2017 yılı itibariyle 22 kişilik kadrosu Ege bölgesinden tüm Türkiye ve dünyaya açılmaya yön tutmuştur. Geniş araç filosuna sahip olan firmamız bu sayede hızlı lojistik imkanına sahiptir.
2015-2016 yılları içerisinde kimyevi maddeler ihracatı kapsamında Türkiye 28. olarak ülke ekonomisine katkıları devam etmektedir 
İSO 9001-2015 kalite yönetim sistemine sahip firmamız , yeni kalite standartları çalışmaları devam etmektedir.
